import { copyFileSync, readFileSync, writeFileSync } from "node:fs";
import { execSync } from "node:child_process";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const source = join(root, "src/config/moderation/prohibited-topics.ts");
const target = join(
  root,
  "supabase/functions/_shared/moderation/prohibited-topics.ts",
);

copyFileSync(source, target);
console.log("Synced moderation policy:", target);

const hardHitSource = join(root, "src/config/moderation/hard-hit-terms.ts");
const hardHitTarget = join(
  root,
  "supabase/functions/_shared/moderation/hard-hit-terms.ts",
);
copyFileSync(hardHitSource, hardHitTarget);
console.log("Synced hard-hit terms:", hardHitTarget);

const boundUserContentSource = join(
  root,
  "src/config/moderation/bound-user-content.ts",
);
const boundUserContentTarget = join(
  root,
  "supabase/functions/_shared/moderation/bound-user-content.ts",
);
copyFileSync(boundUserContentSource, boundUserContentTarget);
console.log("Synced bound-user-content:", boundUserContentTarget);

const prohibitedScanSource = join(root, "src/lib/moderation/prohibited-scan.ts");
const prohibitedScanTarget = join(
  root,
  "supabase/functions/_shared/moderation/prohibited-scan.ts",
);
const prohibitedScanContent = readFileSync(prohibitedScanSource, "utf8")
  .replace(
    "@/config/moderation/hard-hit-terms",
    "./hard-hit-terms.ts",
  )
  .replace(
    "@/config/moderation/prohibited-topics",
    "./prohibited-topics.ts",
  );
writeFileSync(prohibitedScanTarget, prohibitedScanContent);
console.log("Synced prohibited-scan:", prohibitedScanTarget);

const appConfig = readFileSync(join(root, "src/config/app.ts"), "utf8");
const maxMatch = appConfig.match(
  /export const LISTING_DESCRIPTION_MAX_LENGTH = (\d+)/,
);
const descriptionMaxLength = maxMatch?.[1] ?? "2000";
const qaReserveMatch = appConfig.match(
  /export const MODERATION_DESCRIPTION_QA_RESERVE = (\d+)/,
);
const qaReserve = qaReserveMatch?.[1] ?? "400";
const moderationImageMaxMatch = appConfig.match(
  /export const MODERATION_IMAGE_MAX_BYTES = (.+);/,
);
const moderationImagesTotalMatch = appConfig.match(
  /export const MODERATION_IMAGES_MAX_TOTAL_BYTES = (.+);/,
);

function evalBytesExpr(expr, fallback) {
  if (!expr) return fallback;
  try {
    return Number(Function(`"use strict"; return (${expr})`)());
  } catch {
    return fallback;
  }
}

const moderationImageMaxBytes = evalBytesExpr(
  moderationImageMaxMatch?.[1],
  500 * 1024,
);
const moderationImagesTotalBytes = evalBytesExpr(
  moderationImagesTotalMatch?.[1],
  2 * 1024 * 1024,
);

const moderationIndex = readFileSync(
  join(root, "src/config/moderation/index.ts"),
  "utf8",
);
function readModerationConst(name, fallback) {
  const match = moderationIndex.match(
    new RegExp(`export const ${name} = ([^;]+);`),
  );
  if (!match) return fallback;
  try {
    return Function(`"use strict"; return (${match[1]})`)();
  } catch {
    return fallback;
  }
}

const nsfwRawThreshold = readModerationConst("NSFW_NUDITY_RAW_THRESHOLD", 0.6);
const nsfwPartialThreshold = readModerationConst(
  "NSFW_NUDITY_PARTIAL_THRESHOLD",
  0.8,
);
const sightengineTimeoutMs = readModerationConst(
  "SIGHTENGINE_FETCH_TIMEOUT_MS",
  5_000,
);
const hardRejectThreshold = readModerationConst(
  "HARD_REJECT_AUTOBAN_THRESHOLD",
  3,
);
const hardRejectWindowMs = readModerationConst(
  "HARD_REJECT_WINDOW_MS",
  24 * 60 * 60 * 1000,
);
const evidenceBucket = readModerationConst(
  "MODERATION_EVIDENCE_BUCKET",
  "moderation-evidence",
);

const constantsTarget = join(
  root,
  "supabase/functions/_shared/moderation/constants.ts",
);
writeFileSync(
  constantsTarget,
  `// Auto-generated by npm run sync:moderation — do not edit manually.
// Source: src/config/app.ts + src/config/moderation/index.ts

export const LISTING_DESCRIPTION_MAX_LENGTH = ${descriptionMaxLength};

/** Rezerva pro odpovědi z dotazníku (NEEDS_QUESTIONS) — drž v sync s app.ts */
export const MODERATION_DESCRIPTION_QA_RESERVE = ${qaReserve};

/** Max. velikost jedné fotky po dekódování base64 (M7). */
export const MODERATION_IMAGE_MAX_BYTES = ${moderationImageMaxBytes};

/** Max. součet velikostí fotek po dekódování (M7). */
export const MODERATION_IMAGES_MAX_TOTAL_BYTES = ${moderationImagesTotalBytes};

export const NSFW_NUDITY_RAW_THRESHOLD = ${nsfwRawThreshold};

export const NSFW_NUDITY_PARTIAL_THRESHOLD = ${nsfwPartialThreshold};

export const SIGHTENGINE_FETCH_TIMEOUT_MS = ${sightengineTimeoutMs};

export const HARD_REJECT_AUTOBAN_THRESHOLD = ${hardRejectThreshold};

export const HARD_REJECT_WINDOW_MS = ${hardRejectWindowMs};

export const MODERATION_EVIDENCE_BUCKET = ${JSON.stringify(evidenceBucket)};
`,
);
console.log("Synced moderation constants:", constantsTarget);

execSync("npx tsx scripts/export-category-prompts.ts", {
  cwd: root,
  stdio: "inherit",
});
